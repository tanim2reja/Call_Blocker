// Create a CompositionLocal to hold the current theme
val LocalCustomColorScheme = staticCompositionLocalOf {
    //if (isDarkTheme.value) CustomDarkColorScheme else CustomLightColorScheme
    CustomLightColorScheme
}
object CustomTheme {
    val colorScheme: CustomColorScheme
        @Composable
        @ReadOnlyComposable
        get() = LocalCustomColorScheme.current
}

@Composable
fun CustomTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val colorScheme = if (darkTheme) CustomDarkColorScheme else CustomLightColorScheme
    CompositionLocalProvider(
        LocalCustomColorScheme provides colorScheme,
        content = content
    )
}


fun isDynamicColorAvailable(context: Context): Boolean {
    return Build.VERSION.SDK_INT >= Build.VERSION_CODES.S
}





@Composable
fun CallBlockerTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    // Dynamic color is available on Android 12+
    dynamicColor: Boolean = false, //
    content: @Composable() () -> Unit
) {
    val context = LocalContext.current
    isDarkTheme.value = darkTheme
    val useDynamicColor = dynamicColor && isDynamicColorAvailable(context)
    //isSystemInDarkTheme = theme
    val colorScheme = when {
        useDynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        darkTheme -> darkScheme
        else -> lightScheme
    }

    val colorSchemeAppGreen = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        darkTheme -> darkScheme
        else -> lightScheme
    }



    MaterialTheme(
        colorScheme = colorSchemeAppGreen,
        typography = AppTypography,
        content = content
    )
}